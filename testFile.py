
from lab04 import maze_path_exists

def test_path_exists():
    maze = [
        ['+','+','+','+','+','+'],
        ['+','G',' ',' ',' ','+'],
        ['+',' ',' ',' ','+','+'],
        ['+',' ','+','+',' ','+'],
        ['+',' ',' ',' ',' ','+'],
        ['+','+','+','+','+','+']
    ]
    path_found = maze_path_exists(maze, 4, 4)
    assert path_found == True
    assert maze == [
        ['+', '+', '+', '+', '+', '+'],
        ['+', 'G', ' ', ' ', ' ', '+'],
        ['+', 7, ' ', ' ', '+', '+'],
        ['+', 6, '+', '+', 2, '+'],
        ['+', 5, 4, 3, 1, '+'],
        ['+', '+', '+', '+', '+', '+']
    ]

def test_no_path_exists():
    maze = [
        ['+','+','+','+','+','+'],
        ['+',' ',' ',' ',' ','+'],
        ['+',' ','+','+',' ','+'],
        ['+',' ',' ','+',' ','+'],
        ['+','+',' ',' ',' ','+'],
        ['+','+','+','+','+','+']
    ]
    path_found = maze_path_exists(maze, 1, 1)
    assert path_found == False
    assert maze == [
        ['+','+','+','+','+','+'],
        ['+',1,12,11,10,'+'],
        ['+',2,'+','+',9,'+'],
        ['+',3,4,'+',8,'+'],
        ['+','+',5,6,7,'+'],
        ['+','+','+','+','+','+']
    ]
